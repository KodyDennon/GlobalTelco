// ── Color computation ──────────────────────────────────────────────────────

/**
 * Compute the display color for a building based on its connection status.
 * When no coverage data has been applied, falls back to zone-based defaults.
 */
function getBuildingColor(b: BuildingFootprint): [number, number, number, number] {
    // Abandoned buildings always show grey
    if (b.isAbandoned) {
        return [45, 45, 50, 100];
    }

    switch (b.connectionStatus) {
        case 'covered':
            return STATUS_COLORS.covered;
        case 'connected':
            return STATUS_COLORS.connected;
        case 'competitor':
            return STATUS_COLORS.competitor;
        case 'unserved':
            // Only show grey unserved color if coverage data has actually been applied
            // (coverageVersion > 1 means updateBuildingCoverage has run at least once)
            if (coverageVersion > 1) {
                return STATUS_COLORS.unserved;
            }
            // New buildings get a slightly brighter tint
            if (b.isNew) {
                return [38, 42, 56, 210];
            }
            // Fall through to default zone color
            return b.color;
        default:
            return b.color;
    }
}

/**
 * Compute demand overlay color for a building (warm red/orange intensity scale).
 * Connected buildings show their provider color; unserved show demand heat.
 */
function getDemandOverlayColor(b: BuildingFootprint): [number, number, number, number] {
    // Abandoned buildings show very dim
    if (b.isAbandoned) {
        return [40, 40, 45, 60];
    }

    // Connected / covered by player -- show provider green
    if (b.connectionStatus === 'covered' || b.connectionStatus === 'connected') {
        const intensity = Math.min(1.0, b.demandValue / 100);
        return [
            Math.floor(16 + intensity * 14),
            Math.floor(120 + intensity * 65),
            Math.floor(60 + intensity * 69),
            Math.floor(140 + intensity * 60),
        ];
    }

    // Competitor-served -- show competitor red tint
    if (b.connectionStatus === 'competitor') {
        const intensity = Math.min(1.0, b.demandValue / 100);
        return [
            Math.floor(140 + intensity * 60),
            Math.floor(50 + intensity * 30),
            Math.floor(50 + intensity * 20),
            Math.floor(140 + intensity * 60),
        ];
    }

    // Unserved -- warm red/orange based on demand intensity
    const intensity = Math.min(1.0, b.demandValue / 100);
    return [
        Math.floor(200 + intensity * 55),
        Math.floor(80 + intensity * 60),
        Math.floor(20 + intensity * 20),
        Math.floor(120 + intensity * 80),
    ];
}

// ── Layer creation ─────────────────────────────────────────────────────────

/**
 * Create building footprint and street layers.
 * Returns deck.gl PolygonLayers for shadows + building fills, and PathLayers for streets.
 * Visible at zoom 7+.
 */
export function createBuildingsLayers(currentZoom: number, demandOverlayActive: boolean = false): Layer[] {
    if (currentZoom < 7) return [];

    const layers: Layer[] = [];

    // 1. Street layers (underneath buildings)
    if (cachedStreets && cachedStreets.length > 0) {
        const avenues = cachedStreets.filter(s => s.type === 'avenue');
        const sideStreets = cachedStreets.filter(s => s.type === 'street');

        if (avenues.length > 0) {
            layers.push(new PathLayer({
                id: 'streets-avenues',
                data: avenues,
                getPath: (d: StreetSegment) => [d.from, d.to],
                getColor: [80, 80, 90, 160],
                getWidth: 2,
                widthUnits: 'pixels',
                widthMinPixels: 1,
                widthMaxPixels: 3,
                pickable: false,
                parameters: { depthTest: false },
                capRounded: true,
                jointRounded: true,
            }));
        }

        if (sideStreets.length > 0) {
            layers.push(new PathLayer({
                id: 'streets-side',
                data: sideStreets,
                getPath: (d: StreetSegment) => [d.from, d.to],
                getColor: [60, 60, 70, 120],
                getWidth: 1,
                widthUnits: 'pixels',
                widthMinPixels: 1,
                widthMaxPixels: 2,
                pickable: false,
                parameters: { depthTest: false },
                capRounded: true,
                jointRounded: true,
            }));
        }
    }

    if (!cachedBuildings || cachedBuildings.length === 0) return layers;

    // 2. Shadow layer (offset darker polygons underneath buildings)
    if (cachedShadows && cachedShadows.length > 0) {
        layers.push(new PolygonLayer({
            id: 'buildings-shadows',
            data: cachedShadows,
            getPolygon: (d: { polygon: [number, number][] }) => d.polygon,
            getFillColor: (d: { color: [number, number, number, number] }) => d.color,
            stroked: false,
            filled: true,
            pickable: false,
            parameters: { depthTest: false },
            updateTriggers: {
                getFillColor: [cachedCityCount],
            },
        }));
    }

    // 3. Building fill layer -- color depends on coverage status or demand overlay
    const colorFn = demandOverlayActive ? getDemandOverlayColor : getBuildingColor;

    layers.push(new PolygonLayer({
        id: 'buildings-fill',
        data: cachedBuildings,
        getPolygon: (d: BuildingFootprint) => d.polygon,
        getFillColor: (d: BuildingFootprint) => colorFn(d),
        stroked: realEarthMode,
        getLineColor: realEarthMode ? [180, 185, 200, 160] as [number, number, number, number] : [0, 0, 0, 0] as [number, number, number, number],
        getLineWidth: 1,
        lineWidthUnits: 'pixels' as const,
        lineWidthMinPixels: 1,
        filled: true,
        pickable: currentZoom >= 8,
        autoHighlight: currentZoom >= 8,
        highlightColor: [255, 255, 255, 60],
        parameters: { depthTest: false },
        updateTriggers: {
            getFillColor: [cachedCityCount, coverageVersion, demandOverlayActive, realEarthMode],
        },
    }));

    return layers;
}
