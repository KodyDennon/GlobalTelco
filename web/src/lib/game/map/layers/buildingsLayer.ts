// ── Procedural building footprint + intra-city street layer ─────────────────
// Generates building footprints (small rectangle polygons) and intra-city
// streets around city centers for both Real Earth and Procgen modes.
// Buildings serve as demand point visualization and urban fabric.
//
// Building generation is deterministic: seeded from city position so the same
// city always produces the same building layout.
//
// Population tiers determine building count, ring radii, and street density:
//   Hamlet (<50k): 20-50 buildings, main road + 2-3 side streets
//   Town (50k-250k): 50-200 buildings, basic grid/radial core
//   City (250k-1M): 200-500 buildings, full street network
//   Metropolis (1M-5M): 500-1000 buildings, dense streets + multiple districts
//   Megalopolis (5M+): 1000-2000 buildings, very dense streets
//
// Street layout styles (based on cityIndex % 4):
//   0 = Grid (American): rectangular grid with main avenues + cross streets
//   1 = Radial (European): ring roads + radial avenues from center
//   2 = Organic (Asian/Old World): irregular winding streets with branching
//   3 = Mixed: grid in outer areas, organic in inner core
//
// Buildings are arranged along generated streets:
//   Downtown: Dense large rectangles along main avenues
//   Commercial: Medium-large rectangles along side streets
//   Residential Inner: Medium rectangles along residential streets
//   Residential Outer: Small rectangles along suburban roads
//   Suburban: Tiny rectangles (sparse, far out)
//
// Dynamic spawn/destruction:
//   updateBuildingsDynamic() compares current population vs. generation tier
//   Population growth: new buildings spawn on suburban fringe (marked "new")
//   Population decline: outer buildings marked "abandoned" (grey, low opacity)
//
// Revenue Model:
// - NAP auto-coverage: serves all buildings within radius. Revenue = base_rate x demand x 0.85 (15% overhead)
// - Manual drop cable: serves specific building. Revenue = base_rate x demand x 1.0 (no overhead)
// - Manual drops are 15-20% more profitable, rewarding micro-management
// - At large scale, policies auto-manage NAP placement (with overhead deduction)

