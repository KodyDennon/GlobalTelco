/**
 * Generate a single rectangular building footprint polygon.
 * The rectangle is positioned at (cx, cy) with given width/height in degrees,
 * rotated by `angle` radians.
 */
function makeRect(
    cx: number,
    cy: number,
    halfW: number,
    halfH: number,
    angle: number,
): [number, number][] {
    const cos = Math.cos(angle);
    const sin = Math.sin(angle);

    // 4 corners of the rectangle, rotated
    const corners: [number, number][] = [
        [cx + (-halfW * cos - -halfH * sin), cy + (-halfW * sin + -halfH * cos)],
        [cx + (halfW * cos - -halfH * sin), cy + (halfW * sin + -halfH * cos)],
        [cx + (halfW * cos - halfH * sin), cy + (halfW * sin + halfH * cos)],
        [cx + (-halfW * cos - halfH * sin), cy + (-halfW * sin + halfH * cos)],
    ];
    // Close the ring
    corners.push(corners[0]);
    return corners;
}

/**
 * Classify which zone a given distance from center belongs to, at a given population.
 */
function classifyZone(distFromCenter: number, population: number): BuildingZone {
    for (const config of ZONE_CONFIGS) {
        const { outerR } = zoneRadii(config.zone, population);
        if (distFromCenter <= outerR) return config.zone;
    }
    return 'suburban';
}

/**
 * Get building spacing along streets based on zone.
 * Returns spacing in degrees.
 */
function buildingSpacingForZone(zone: BuildingZone): number {
    switch (zone) {
        case 'downtown': return 20 * DEG_PER_METER;        // 20m
        case 'commercial': return 35 * DEG_PER_METER;       // 35m
        case 'residential_inner': return 40 * DEG_PER_METER; // 40m
        case 'residential_outer': return 60 * DEG_PER_METER; // 60m
        case 'suburban': return 100 * DEG_PER_METER;         // 100m
    }
}

/**
 * Get building setback from street centerline in degrees.
 */
function buildingSetbackForZone(zone: BuildingZone): number {
    switch (zone) {
        case 'downtown': return 10 * DEG_PER_METER;
        case 'commercial': return 15 * DEG_PER_METER;
        case 'residential_inner': return 20 * DEG_PER_METER;
        case 'residential_outer': return 25 * DEG_PER_METER;
        case 'suburban': return 30 * DEG_PER_METER;
    }
}

/**
 * Generate buildings aligned to CityStreet geometry from roadsLayer.
 * CityStreets have multi-point paths and typed widths (avenue/main/residential/alley).
 * Buildings are placed along each path segment with appropriate zone sizing:
 *   - avenue: downtown/commercial zone buildings (large footprints)
 *   - main: commercial zone buildings
 *   - residential: residential zone buildings (set back from street)
 *   - alley: small residential/suburban buildings
 */
function generateBuildingsAlongCityStreets(
    city: City,
    cityStreets: CityStreet[],
    maxCount: number,
    rng: () => number,
): BuildingFootprint[] {
    const buildings: BuildingFootprint[] = [];
    const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));

    // Map CityStreet type to building zone
    const streetTypeToZone: Record<string, BuildingZone> = {
        avenue: 'downtown',
        main: 'commercial',
        residential: 'residential_inner',
        alley: 'residential_outer',
    };

    for (const street of cityStreets) {
        if (buildings.length >= maxCount) break;

        const zone = streetTypeToZone[street.type] ?? 'residential_inner';
        const spacing = buildingSpacingForZone(zone);
        const setback = buildingSetbackForZone(zone);
        const config = ZONE_CONFIGS.find(c => c.zone === zone) ?? ZONE_CONFIGS[0];

        // Walk along each segment of the multi-point path
        for (let si = 0; si < street.path.length - 1 && buildings.length < maxCount; si++) {
            const p0 = street.path[si];
            const p1 = street.path[si + 1];
            const dx = p1[0] - p0[0];
            const dy = p1[1] - p0[1];
            const segLen = Math.sqrt(dx * dx + dy * dy);
            if (segLen < 1e-8) continue;

            const ndx = dx / segLen;
            const ndy = dy / segLen;
            const perpX = -ndy;
            const perpY = ndx;
            const streetAngle = Math.atan2(dy, dx);

            // Determine zone at segment midpoint (override if too far from center)
            const midX = (p0[0] + p1[0]) / 2;
            const midY = (p0[1] + p1[1]) / 2;
            const distFromCenter = Math.sqrt(
                ((midX - city.x) / latCorr) ** 2 + (midY - city.y) ** 2
            );
            const actualZone = classifyZone(distFromCenter, city.population);
            // Use the more appropriate zone (whichever is further from downtown)
            const effectiveZone = ZONE_CONFIGS.findIndex(c => c.zone === actualZone) >
                ZONE_CONFIGS.findIndex(c => c.zone === zone) ? actualZone : zone;
            const effectiveConfig = ZONE_CONFIGS.find(c => c.zone === effectiveZone) ?? config;
            const effectiveSpacing = buildingSpacingForZone(effectiveZone);
            const effectiveSetback = buildingSetbackForZone(effectiveZone);

            const numBuildings = Math.floor(segLen / effectiveSpacing);
            for (let i = 0; i < numBuildings && buildings.length < maxCount; i++) {
                const t = (i + 0.5) / Math.max(1, numBuildings);
                const baseX = p0[0] + dx * t;
                const baseY = p0[1] + dy * t;

                for (const side of [-1, 1]) {
                    if (buildings.length >= maxCount) break;

                    const along = (rng() - 0.5) * effectiveSpacing * 0.3;
                    const setbackVar = effectiveSetback * (0.8 + rng() * 0.4);
                    const bx = baseX + ndx * along + perpX * setbackVar * side;
                    const by = baseY + ndy * along + perpY * setbackVar * side;

                    const sizeW = effectiveConfig.minSizeDeg + rng() * (effectiveConfig.maxSizeDeg - effectiveConfig.minSizeDeg);
                    const aspect = effectiveConfig.minAspect + rng() * (effectiveConfig.maxAspect - effectiveConfig.minAspect);
                    const sizeH = sizeW * aspect;
                    const halfW = (sizeW * latCorr) / 2;
                    const halfH = sizeH / 2;

                    const angleJitter = (rng() - 0.5) * 0.15;
                    const angle = streetAngle + angleJitter;

                    const baseColor = ZONE_COLORS[effectiveZone];
                    const colorVariation = Math.floor((rng() - 0.5) * 12);
                    const color: [number, number, number, number] = [
                        Math.max(0, Math.min(255, baseColor[0] + colorVariation)),
                        Math.max(0, Math.min(255, baseColor[1] + colorVariation)),
                        Math.max(0, Math.min(255, baseColor[2] + colorVariation)),
                        baseColor[3],
                    ];

                    buildings.push({
                        polygon: makeRect(bx, by, halfW, halfH, angle),
                        color,
                        cx: bx,
                        cy: by,
                        zone: effectiveZone,
                        demandValue: ZONE_DEMAND[effectiveZone],
                        connectionStatus: 'unserved',
                        providerCorpId: 0,
                        cityId: city.id,
                    });
                }
            }
        }
    }

    return buildings;
}

/**
 * Filter CityStreets from roadsLayer that belong to (are near) a specific city.
 * Uses distance-from-center check based on city radius.
 */
function filterCityStreetsForCity(city: City, allStreets: CityStreet[]): CityStreet[] {
    const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));
    // Use a generous radius based on population
    const maxR = cityRadiusForFilter(city.population);

    return allStreets.filter(street => {
        // Check if the midpoint of the first segment is within the city
        if (street.path.length < 2) return false;
        const mx = (street.path[0][0] + street.path[Math.min(1, street.path.length - 1)][0]) / 2;
        const my = (street.path[0][1] + street.path[Math.min(1, street.path.length - 1)][1]) / 2;
        const dist = Math.sqrt(((mx - city.x) / latCorr) ** 2 + (my - city.y) ** 2);
        return dist <= maxR;
    });
}

/**
 * Estimate city radius in degrees for street filtering.
 */
function cityRadiusForFilter(population: number): number {
    if (population >= 5_000_000) return 0.35;
    if (population >= 1_000_000) return 0.22;
    if (population >= 250_000) return 0.14;
    if (population >= 50_000) return 0.07;
    return 0.04;
}

/**
 * Generate buildings placed along the given street segments.
 * Buildings are placed on both sides of each street, with appropriate
 * setback and spacing based on their zone classification.
 */
function generateBuildingsAlongStreets(
    city: City,
    streets: StreetSegment[],
    maxCount: number,
    rng: () => number,
): BuildingFootprint[] {
    const buildings: BuildingFootprint[] = [];
    const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));

    for (const seg of streets) {
        if (buildings.length >= maxCount) break;

        // Compute street direction and length
        const dx = seg.to[0] - seg.from[0];
        const dy = seg.to[1] - seg.from[1];
        const streetLen = Math.sqrt(dx * dx + dy * dy);
        if (streetLen < 1e-8) continue;

        // Normalized direction and perpendicular
        const ndx = dx / streetLen;
        const ndy = dy / streetLen;
        const perpX = -ndy;
        const perpY = ndx;

        // Street angle for aligning buildings
        const streetAngle = Math.atan2(dy, dx);

        // Determine zone at street midpoint
        const midX = (seg.from[0] + seg.to[0]) / 2;
        const midY = (seg.from[1] + seg.to[1]) / 2;
        const distFromCenter = Math.sqrt(
            ((midX - city.x) / latCorr) ** 2 + (midY - city.y) ** 2
        );
        const zone = classifyZone(distFromCenter, city.population);
        const spacing = buildingSpacingForZone(zone);
        const setback = buildingSetbackForZone(zone);

        // Find the matching ZoneConfig for building sizes
        const config = ZONE_CONFIGS.find(c => c.zone === zone) ?? ZONE_CONFIGS[0];

        // Walk along the street and place buildings on both sides
        const numBuildings = Math.floor(streetLen / spacing);
        for (let i = 0; i < numBuildings && buildings.length < maxCount; i++) {
            const t = (i + 0.5) / numBuildings;
            const baseX = seg.from[0] + dx * t;
            const baseY = seg.from[1] + dy * t;

            // Place on both sides of the street (left and right)
            for (const side of [-1, 1]) {
                if (buildings.length >= maxCount) break;

                // Slight random offset along street
                const along = (rng() - 0.5) * spacing * 0.3;
                // Random setback variation
                const setbackVar = setback * (0.8 + rng() * 0.4);

                const bx = baseX + ndx * along + perpX * setbackVar * side;
                const by = baseY + ndy * along + perpY * setbackVar * side;

                // Building size from zone config
                const sizeW = config.minSizeDeg + rng() * (config.maxSizeDeg - config.minSizeDeg);
                const aspect = config.minAspect + rng() * (config.maxAspect - config.minAspect);
                const sizeH = sizeW * aspect;
                const halfW = (sizeW * latCorr) / 2;
                const halfH = sizeH / 2;

                // Align building to street with slight jitter
                const angleJitter = (rng() - 0.5) * 0.15; // +/- ~8 degrees
                const angle = streetAngle + angleJitter;

                // Color
                const baseColor = ZONE_COLORS[zone];
                const colorVariation = Math.floor((rng() - 0.5) * 12);
                const color: [number, number, number, number] = [
                    Math.max(0, Math.min(255, baseColor[0] + colorVariation)),
                    Math.max(0, Math.min(255, baseColor[1] + colorVariation)),
                    Math.max(0, Math.min(255, baseColor[2] + colorVariation)),
                    baseColor[3],
                ];

                buildings.push({
                    polygon: makeRect(bx, by, halfW, halfH, angle),
                    color,
                    cx: bx,
                    cy: by,
                    zone,
                    demandValue: ZONE_DEMAND[zone],
                    connectionStatus: 'unserved',
                    providerCorpId: 0,
                    cityId: city.id,
                });
            }
        }
    }

    return buildings;
}

/**
 * Fallback: Generate buildings in concentric rings (original method).
 * Used for very small cities without streets, or as a filler.
 */
function generateCityBuildingsRadial(city: City, maxCount: number, rng: () => number): BuildingFootprint[] {
    const buildings: BuildingFootprint[] = [];
    const latCorrectionFactor = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));

    for (const config of ZONE_CONFIGS) {
        const zoneCount = Math.floor(maxCount * config.fraction);
        const { innerR, outerR } = zoneRadii(config.zone, city.population);

        // Skip zone if radius is negligible
        if (outerR < 0.0001) continue;

        for (let i = 0; i < zoneCount && buildings.length < maxCount; i++) {
            // Random position within the ring (polar coordinates)
            const r = innerR + rng() * (outerR - innerR);
            const theta = rng() * Math.PI * 2;

            const bx = city.x + r * Math.cos(theta) * latCorrectionFactor;
            const by = city.y + r * Math.sin(theta);

            // Random building size within zone constraints
            const sizeW = config.minSizeDeg + rng() * (config.maxSizeDeg - config.minSizeDeg);
            const aspect = config.minAspect + rng() * (config.maxAspect - config.minAspect);
            const sizeH = sizeW * aspect;

            // Apply lat correction to width
            const halfW = (sizeW * latCorrectionFactor) / 2;
            const halfH = sizeH / 2;

            // Random rotation (slight, mostly grid-aligned with some randomness)
            const baseAngle = Math.floor(rng() * 4) * (Math.PI / 2);
            const angleJitter = (rng() - 0.5) * 0.3; // +/- ~17 degrees
            const angle = baseAngle + angleJitter;

            // Default color from zone palette with slight per-building variation
            const baseColor = ZONE_COLORS[config.zone];
            const colorVariation = Math.floor((rng() - 0.5) * 12);
            const color: [number, number, number, number] = [
                Math.max(0, Math.min(255, baseColor[0] + colorVariation)),
                Math.max(0, Math.min(255, baseColor[1] + colorVariation)),
                Math.max(0, Math.min(255, baseColor[2] + colorVariation)),
                baseColor[3],
            ];

            buildings.push({
                polygon: makeRect(bx, by, halfW, halfH, angle),
                color,
                cx: bx,
                cy: by,
                zone: config.zone,
                demandValue: ZONE_DEMAND[config.zone],
                connectionStatus: 'unserved',
                providerCorpId: 0,
                cityId: city.id,
            });
        }
    }

    return buildings;
}

/**
 * Generate all building footprints for a single city.
 * When roadsLayer city streets are available, buildings align to that geometry
 * (avenue = downtown, main = commercial, residential = inner residential,
 * alley = outer residential). Falls back to internal street generation or
 * radial placement when roadsLayer data is unavailable.
 */
function generateCityBuildings(city: City, cityIndex: number): { buildings: BuildingFootprint[]; streets: StreetSegment[] } {
    const count = buildingCount(city.population);
    const tier = getPopulationTier(city.population);
    const rng = mulberry32(citySeed(city));

    // Try to use roadsLayer city streets for building alignment (Phase 3.2.3)
    const allCityStreets = getCachedCityStreets();
    if (allCityStreets && allCityStreets.length > 0) {
        const cityStreets = filterCityStreetsForCity(city, allCityStreets);
        if (cityStreets.length > 0) {
            // Generate buildings aligned to roadsLayer street geometry
            const streetBuildings = generateBuildingsAlongCityStreets(city, cityStreets, count, rng);

            // Fill remaining with radial placement if streets didn't yield enough
            if (streetBuildings.length < count) {
                const remaining = count - streetBuildings.length;
                const fillRng = mulberry32((citySeed(city) + 31337) | 0);
                const fillBuildings = generateCityBuildingsRadial(city, remaining, fillRng);
                return {
                    buildings: [...streetBuildings, ...fillBuildings],
                    streets: [], // streets rendered by roadsLayer, not here
                };
            }

            return {
                buildings: streetBuildings.slice(0, count),
                streets: [], // streets rendered by roadsLayer, not here
            };
        }
    }

    // Fallback: generate internal streets and align buildings to them
    const streets = generateCityStreets(city, cityIndex, tier);

    if (streets.length === 0) {
        // No streets (very tiny city) -- use pure radial placement
        return {
            buildings: generateCityBuildingsRadial(city, count, rng),
            streets: [],
        };
    }

    // Generate buildings along internal streets
    const streetBuildings = generateBuildingsAlongStreets(city, streets, count, rng);

    // If streets didn't generate enough buildings, fill remaining with radial placement
    if (streetBuildings.length < count) {
        const remaining = count - streetBuildings.length;
        // Use a secondary RNG to avoid disturbing the main sequence
        const fillRng = mulberry32((citySeed(city) + 31337) | 0);
        const fillBuildings = generateCityBuildingsRadial(city, remaining, fillRng);
        return {
            buildings: [...streetBuildings, ...fillBuildings],
            streets,
        };
    }

    return {
        buildings: streetBuildings.slice(0, count),
        streets,
    };
}

// ── Cached data ────────────────────────────────────────────────────────────

let cachedBuildings: BuildingFootprint[] | null = null;
let cachedShadows: { polygon: [number, number][]; color: [number, number, number, number] }[] | null = null;
let cachedStreets: StreetSegment[] | null = null;
let cachedCityCount: number = 0;
/** Monotonic version counter -- incremented on data or coverage changes. */
let coverageVersion: number = 0;
/** Track population tiers at generation time for dynamic updates. */
let cachedCityTiers: Map<number, PopulationTier> = new Map();

/**
 * Build and cache building footprint and street data from city array.
 * Call once after cities are loaded from WASM.
 *
 * Enforces the MAX_VISIBLE_BUILDINGS budget by generating buildings for
 * the largest cities first and stopping when the budget is reached.
 * Enforces the MAX_STREET_SEGMENTS budget similarly.
 */
export function buildBuildingData(cities: City[]): void {
    if (cities.length === 0) {
        cachedBuildings = [];
        cachedShadows = [];
        cachedStreets = [];
        cachedCityCount = 0;
        cachedCityTiers.clear();
        return;
    }

    // Process cities from largest to smallest so we prioritize big cities
    const sorted = [...cities]
        .filter(c => Math.abs(c.y) <= 85)
        .sort((a, b) => b.population - a.population);

    // Build a deterministic city index map (sorted by ID for determinism)
    const cityIndexMap = new Map<number, number>();
    const citiesByIdOrder = [...cities].sort((a, b) => a.id - b.id);
    citiesByIdOrder.forEach((c, i) => cityIndexMap.set(c.id, i));

    const allBuildings: BuildingFootprint[] = [];
    const allShadows: { polygon: [number, number][]; color: [number, number, number, number] }[] = [];
    const allStreets: StreetSegment[] = [];
    const newTiers = new Map<number, PopulationTier>();

    for (const city of sorted) {
        if (allBuildings.length >= MAX_VISIBLE_BUILDINGS && allStreets.length >= MAX_STREET_SEGMENTS) break;

        const cityIdx = cityIndexMap.get(city.id) ?? 0;
        const { buildings: cityBuildings, streets: cityStreets } = generateCityBuildings(city, cityIdx);
        const tier = getPopulationTier(city.population);
        newTiers.set(city.id, tier);

        // Add streets within budget
        if (allStreets.length < MAX_STREET_SEGMENTS) {
            const streetRemaining = MAX_STREET_SEGMENTS - allStreets.length;
            const streetsToAdd = cityStreets.slice(0, streetRemaining);
            for (const s of streetsToAdd) {
                allStreets.push(s);
            }
        }

        // Add buildings within budget
        if (allBuildings.length < MAX_VISIBLE_BUILDINGS) {
            const remaining = MAX_VISIBLE_BUILDINGS - allBuildings.length;
            const toAdd = cityBuildings.slice(0, remaining);

            for (const b of toAdd) {
                allBuildings.push(b);

                // Generate shadow: offset polygon slightly SE and darker
                const shadowOffset = 0.00003; // ~3m offset
                const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));
                const shadowPoly = b.polygon.map(([lon, lat]) => [
                    lon + shadowOffset * latCorr,
                    lat - shadowOffset,
                ] as [number, number]);

                allShadows.push({
                    polygon: shadowPoly,
                    color: [8, 10, 16, 120],
                });
            }
        }
    }

    cachedBuildings = allBuildings;
    cachedShadows = allShadows;
    cachedStreets = allStreets;
    cachedCityCount = cities.length;
    cachedCityTiers = newTiers;
    coverageVersion++;
}

/** Dispose cached building data to free memory. */
export function disposeBuildingData(): void {
    cachedBuildings = null;
    cachedShadows = null;
    cachedStreets = null;
    cachedCityCount = 0;
    cachedCityTiers.clear();
}

// ── Dynamic building spawn/destruction (Gap #14) ──────────────────────────

/**
 * Update buildings dynamically based on population changes.
 * Call when city data updates (every ~30 ticks).
 *
 * - If population grew across a tier threshold: add new buildings on suburban fringe
 * - If population declined: mark outer buildings as "abandoned" (grey, low opacity)
 *
 * Returns true if buildings were modified (layer needs update trigger).
 */
export function updateBuildingsDynamic(cities: City[]): boolean {
    if (!cachedBuildings || cachedBuildings.length === 0) return false;

    let modified = false;

    for (const city of cities) {
        const oldTier = cachedCityTiers.get(city.id);
        const newTier = getPopulationTier(city.population);

        if (!oldTier || oldTier === newTier) continue;

        const tierOrder: PopulationTier[] = ['hamlet', 'town', 'city', 'metro', 'megalopolis'];
        const oldIdx = tierOrder.indexOf(oldTier);
        const newIdx = tierOrder.indexOf(newTier);

        if (newIdx > oldIdx) {
            // Population GREW -- add new buildings on the suburban fringe
            const numNew = Math.min(20, MAX_VISIBLE_BUILDINGS - cachedBuildings.length);
            if (numNew <= 0) continue;

            const rng = mulberry32((citySeed(city) + 99991) | 0);
            const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));
            const { innerR, outerR } = zoneRadii('suburban', city.population);

            for (let i = 0; i < numNew; i++) {
                const r = innerR + rng() * (outerR - innerR);
                const theta = rng() * Math.PI * 2;
                const bx = city.x + r * Math.cos(theta) * latCorr;
                const by = city.y + r * Math.sin(theta);

                const config = ZONE_CONFIGS[4]; // suburban
                const sizeW = config.minSizeDeg + rng() * (config.maxSizeDeg - config.minSizeDeg);
                const aspect = config.minAspect + rng() * (config.maxAspect - config.minAspect);
                const sizeH = sizeW * aspect;
                const halfW = (sizeW * latCorr) / 2;
                const halfH = sizeH / 2;
                const angle = rng() * Math.PI;

                // Slightly brighter color to indicate "new" construction
                const color: [number, number, number, number] = [38, 42, 56, 210];

                cachedBuildings.push({
                    polygon: makeRect(bx, by, halfW, halfH, angle),
                    color,
                    cx: bx,
                    cy: by,
                    zone: 'suburban',
                    demandValue: ZONE_DEMAND.suburban,
                    connectionStatus: 'unserved',
                    providerCorpId: 0,
                    isNew: true,
                    cityId: city.id,
                });

                // Add shadow
                if (cachedShadows) {
                    const shadowOffset = 0.00003;
                    const shadowPoly = cachedBuildings[cachedBuildings.length - 1].polygon.map(([lon, lat]) => [
                        lon + shadowOffset * latCorr,
                        lat - shadowOffset,
                    ] as [number, number]);
                    cachedShadows.push({
                        polygon: shadowPoly,
                        color: [8, 10, 16, 120],
                    });
                }
            }

            cachedCityTiers.set(city.id, newTier);
            modified = true;
        } else if (newIdx < oldIdx) {
            // Population DECLINED -- mark some outer buildings as abandoned
            const cityBuildings = cachedBuildings.filter(b => b.cityId === city.id);
            // Sort by distance from center (descending) and mark outermost as abandoned
            const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));
            const withDist = cityBuildings.map(b => ({
                b,
                dist: Math.sqrt(((b.cx - city.x) / latCorr) ** 2 + (b.cy - city.y) ** 2),
            }));
            withDist.sort((a, b) => b.dist - a.dist);

            // Mark ~10% of outer buildings as abandoned
            const numAbandon = Math.max(1, Math.floor(cityBuildings.length * 0.1));
            for (let i = 0; i < Math.min(numAbandon, withDist.length); i++) {
                withDist[i].b.isAbandoned = true;
                withDist[i].b.color = [45, 45, 50, 100]; // grey, low opacity
            }

            cachedCityTiers.set(city.id, newTier);
            modified = true;
        }
    }

    if (modified) {
        coverageVersion++;
    }

    return modified;
}

// ── Coverage update ──────────────────────────────────────────────────────

/**
 * Haversine-approximated squared distance in meters between two lon/lat points.
 * Uses flat-earth approximation for speed (accurate enough within ~50km).
 */
function distSqMeters(lon1: number, lat1: number, lon2: number, lat2: number): number {
    const avgLat = ((lat1 + lat2) / 2) * (Math.PI / 180);
    const cosLat = Math.cos(avgLat);
    const dx = (lon2 - lon1) * cosLat * 111_320;
    const dy = (lat2 - lat1) * 111_320;
    return dx * dx + dy * dy;
}

/**
 * Update building coverage status based on infrastructure node positions.
 *
 * Coverage rules:
 *   - NetworkAccessPoint (NAP): 500m radius, marks buildings as 'covered'
 *   - FiberDistributionHub (FDH): 2000m radius, marks buildings as 'covered'
 *   - Other node types with coverage_factor > 0.5: 300m radius (e.g. SmallCell, CellTower)
 *   - Buildings served by non-player corps get 'competitor' status
 *
 * @param playerNodes Array of infrastructure nodes (from bridge.getAllInfrastructure().nodes)
 * @param playerCorpId The player's corporation ID
 */
export function updateBuildingCoverage(
    playerNodes: Array<{ x: number; y: number; node_type: string; owner: number }>,
    playerCorpId: number
): void {
    if (!cachedBuildings || cachedBuildings.length === 0) return;

    // Reset all buildings to unserved
    for (const b of cachedBuildings) {
        b.connectionStatus = 'unserved';
        b.providerCorpId = 0;
    }

    // Coverage radius in meters per node type
    const NODE_COVERAGE: Record<string, number> = {
        'NetworkAccessPoint': 500,
        'SmallCell': 400,
        'CellTower': 800,
        'FiberDistributionHub': 2000,
        'MacroCell': 1200,
        'WirelessRelay': 600,
    };

    // Squared radii for faster comparison
    const coverageNodes = playerNodes
        .filter(n => NODE_COVERAGE[n.node_type] !== undefined)
        .map(n => ({
            x: n.x,
            y: n.y,
            owner: n.owner,
            radiusSqM: (NODE_COVERAGE[n.node_type]!) ** 2,
        }));

    if (coverageNodes.length === 0) return;

    for (const b of cachedBuildings) {
        for (const node of coverageNodes) {
            const dSq = distSqMeters(b.cx, b.cy, node.x, node.y);
            if (dSq <= node.radiusSqM) {
                if (node.owner === playerCorpId) {
                    // Player coverage -- only upgrade, never downgrade
                    if (b.connectionStatus !== 'connected') {
                        b.connectionStatus = 'covered';
                        b.providerCorpId = playerCorpId;
                    }
                } else {
                    // Competitor coverage -- only set if not already covered by player
                    if (b.connectionStatus === 'unserved') {
                        b.connectionStatus = 'competitor';
                        b.providerCorpId = node.owner;
                    }
                }
            }
        }
    }

    coverageVersion++;
}

