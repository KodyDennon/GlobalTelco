// ── Seeded PRNG (deterministic per city) ──────────────────────────────────

/**
 * Simple mulberry32 PRNG seeded from a 32-bit integer.
 * Returns a function that produces the next pseudorandom float in [0, 1).
 */
function mulberry32(seed: number): () => number {
    let state = seed | 0;
    return () => {
        state = (state + 0x6D2B79F5) | 0;
        let t = Math.imul(state ^ (state >>> 15), 1 | state);
        t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
        return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
    };
}

/**
 * Create a deterministic seed from a city's position.
 * Combines lon and lat into a single 32-bit integer.
 */
function citySeed(city: City): number {
    // Mix lat/lon bits for a good spread
    const lonBits = Math.floor((city.x + 180) * 10000);
    const latBits = Math.floor((city.y + 90) * 10000);
    return (lonBits * 2654435761 + latBits * 340573321) | 0;
}

// ── Building count by population tier ─────────────────────────────────────

function buildingCount(population: number): number {
    if (population >= 5000000) return 1000 + Math.min(1000, Math.floor(population / 10000000 * 1000));
    if (population >= 1000000) return 500 + Math.floor((population - 1000000) / 4000000 * 500);
    if (population >= 250000) return 200 + Math.floor((population - 250000) / 750000 * 300);
    if (population >= 50000) return 50 + Math.floor((population - 50000) / 200000 * 150);
    return 20 + Math.floor(population / 50000 * 30);
}

// ── Population tier classification ───────────────────────────────────────

function getPopulationTier(population: number): PopulationTier {
    if (population >= 5000000) return 'megalopolis';
    if (population >= 1000000) return 'metro';
    if (population >= 250000) return 'city';
    if (population >= 50000) return 'town';
    return 'hamlet';
}

function getCityLayout(cityIndex: number): CityLayout {
    const layouts: CityLayout[] = ['grid', 'radial', 'organic', 'mixed'];
    return layouts[cityIndex % 4];
}

// ── Population-scaled zone radii ──────────────────────────────────────────

/**
 * Compute a population interpolation factor [0, 1].
 * 0 at pop <= 50k, 1 at pop >= 10M.
 */
export function popFactor(population: number): number {
    const MIN_POP = 50_000;
    const MAX_POP = 10_000_000;
    if (population <= MIN_POP) return 0;
    if (population >= MAX_POP) return 1;
    return (population - MIN_POP) / (MAX_POP - MIN_POP);
}

function lerp(a: number, b: number, t: number): number {
    return a + (b - a) * t;
}

/**
 * Get inner and outer radii for a zone at a given population.
 */
export function zoneRadii(zone: BuildingZone, population: number): { innerR: number; outerR: number } {
    const t = popFactor(population);
    const bounds = ZONE_RADII[zone];
    return {
        innerR: lerp(bounds.innerMin, bounds.innerMax, t),
        outerR: lerp(bounds.outerMin, bounds.outerMax, t),
    };
}

// ── Intra-city street generation ──────────────────────────────────────────

/** Degrees per meter at equator (approximate). 1 degree ~ 111,320m */
const DEG_PER_METER = 1 / 111_320;

/**
 * Generate intra-city streets based on layout style and population tier.
 * Returns an array of StreetSegment objects.
 *
 * Street generation parameters scale with population:
 *   Hamlet (<50k): main road + 2-3 side streets
 *   Town (50k-250k): basic grid/radial core + some streets
 *   City (250k-1M): full street network
 *   Metro (1M+): dense streets, multiple districts
 */
export function generateCityStreets(
    city: City,
    cityIndex: number,
    populationTier: PopulationTier,
): StreetSegment[] {
    const layout = getCityLayout(cityIndex);
    const rng = mulberry32((citySeed(city) + 7919) | 0); // offset seed so streets differ from buildings
    const latCorr = 1 / Math.max(0.1, Math.cos((city.y * Math.PI) / 180));

    // City extent radius in degrees (based on outermost zone)
    const { outerR: cityRadius } = zoneRadii('suburban', city.population);
    if (cityRadius < 0.0001) return [];

    const segments: StreetSegment[] = [];

    switch (layout) {
        case 'grid':
            generateGridStreets(city, rng, latCorr, cityRadius, populationTier, segments);
            break;
        case 'radial':
            generateRadialStreets(city, rng, latCorr, cityRadius, populationTier, segments);
            break;
        case 'organic':
            generateOrganicStreets(city, rng, latCorr, cityRadius, populationTier, segments);
            break;
        case 'mixed':
            // Organic in the inner core (downtown + commercial radius)
            {
                const innerRadius = zoneRadii('commercial', city.population).outerR;
                generateOrganicStreets(city, rng, latCorr, innerRadius, populationTier, segments);
            }
            // Grid in the outer areas
            generateGridStreets(city, rng, latCorr, cityRadius, populationTier, segments, 0.3);
            break;
    }

    return segments;
}

/**
 * Grid (American) layout: rectangular street grid within city radius.
 * Main avenues every ~500m (wide), cross streets every ~200m (narrow).
 * Grid is rotated by a seed-based angle.
 */
function generateGridStreets(
    city: City,
    rng: () => number,
    latCorr: number,
    cityRadius: number,
    tier: PopulationTier,
    out: StreetSegment[],
    startFraction: number = 0,
): void {
    // Rotation angle for the grid (seed-based, slight)
    const gridAngle = (rng() - 0.5) * 0.4; // +/- ~23 degrees
    const cos = Math.cos(gridAngle);
    const sin = Math.sin(gridAngle);

    // Spacing in degrees
    const avenueSpacingDeg = 500 * DEG_PER_METER;  // ~500m between avenues
    const streetSpacingDeg = 200 * DEG_PER_METER;   // ~200m between cross streets

    // Determine grid extent based on tier
    let gridExtent = cityRadius * 0.6; // default: 60% of city radius
    if (tier === 'hamlet') gridExtent = cityRadius * 0.2;
    else if (tier === 'town') gridExtent = cityRadius * 0.35;
    else if (tier === 'metro' || tier === 'megalopolis') gridExtent = cityRadius * 0.75;

    const startExtent = gridExtent * startFraction;

    // Generate E-W avenues
    const numAvenues = Math.floor((gridExtent * 2) / avenueSpacingDeg);
    for (let i = 0; i <= numAvenues; i++) {
        const yOff = -gridExtent + i * avenueSpacingDeg;
        if (Math.abs(yOff) < startExtent) continue;
        // Line from -gridExtent to +gridExtent along rotated axis
        const x1 = -gridExtent;
        const x2 = gridExtent;
        // Rotate and translate
        const from: [number, number] = [
            city.x + (x1 * cos - yOff * sin) * latCorr,
            city.y + (x1 * sin + yOff * cos),
        ];
        const to: [number, number] = [
            city.x + (x2 * cos - yOff * sin) * latCorr,
            city.y + (x2 * sin + yOff * cos),
        ];
        out.push({ from, to, type: 'avenue' });
    }

    // Generate N-S cross streets
    const numStreets = Math.floor((gridExtent * 2) / streetSpacingDeg);
    // For hamlets, limit cross streets
    const maxCrossStreets = tier === 'hamlet' ? 3 : tier === 'town' ? Math.min(numStreets, 15) : numStreets;
    const step = numStreets > maxCrossStreets ? Math.floor(numStreets / maxCrossStreets) : 1;
    for (let i = 0; i <= numStreets; i += step) {
        const xOff = -gridExtent + i * streetSpacingDeg;
        if (Math.abs(xOff) < startExtent) continue;
        const y1 = -gridExtent;
        const y2 = gridExtent;
        const from: [number, number] = [
            city.x + (xOff * cos - y1 * sin) * latCorr,
            city.y + (xOff * sin + y1 * cos),
        ];
        const to: [number, number] = [
            city.x + (xOff * cos - y2 * sin) * latCorr,
            city.y + (xOff * sin + y2 * cos),
        ];
        out.push({ from, to, type: 'street' });
    }
}

/**
 * Radial (European) layout: ring roads at increasing radii + radial avenues.
 * Central plaza at core. Ring roads every ~300m, radial avenues every 30-45 degrees.
 */
function generateRadialStreets(
    city: City,
    rng: () => number,
    latCorr: number,
    cityRadius: number,
    tier: PopulationTier,
    out: StreetSegment[],
): void {
    const ringSpacingDeg = 300 * DEG_PER_METER; // ~300m between ring roads

    // Determine number of rings and radial avenues based on tier
    let maxRadius = cityRadius * 0.6;
    let numRadials = 8;
    if (tier === 'hamlet') { maxRadius = cityRadius * 0.15; numRadials = 4; }
    else if (tier === 'town') { maxRadius = cityRadius * 0.3; numRadials = 6; }
    else if (tier === 'metro' || tier === 'megalopolis') { maxRadius = cityRadius * 0.75; numRadials = 12; }

    const numRings = Math.max(1, Math.floor(maxRadius / ringSpacingDeg));
    const radialAngleStep = (Math.PI * 2) / numRadials;
    const radialBaseAngle = rng() * Math.PI * 2; // random rotation offset

    // Generate ring roads (approximated as N-gon segments)
    const ringSegments = Math.max(16, numRadials * 2); // segments per ring
    for (let ring = 1; ring <= numRings; ring++) {
        const r = ring * ringSpacingDeg;
        const angleStep = (Math.PI * 2) / ringSegments;
        for (let seg = 0; seg < ringSegments; seg++) {
            const a1 = seg * angleStep;
            const a2 = (seg + 1) * angleStep;
            const from: [number, number] = [
                city.x + r * Math.cos(a1) * latCorr,
                city.y + r * Math.sin(a1),
            ];
            const to: [number, number] = [
                city.x + r * Math.cos(a2) * latCorr,
                city.y + r * Math.sin(a2),
            ];
            out.push({ from, to, type: ring <= 2 ? 'avenue' : 'street' });
        }
    }

    // Generate radial avenues from center to edge
    for (let i = 0; i < numRadials; i++) {
        const angle = radialBaseAngle + i * radialAngleStep;
        // Start from first ring (central plaza)
        const startR = ringSpacingDeg * 0.3;
        const from: [number, number] = [
            city.x + startR * Math.cos(angle) * latCorr,
            city.y + startR * Math.sin(angle),
        ];
        const to: [number, number] = [
            city.x + maxRadius * Math.cos(angle) * latCorr,
            city.y + maxRadius * Math.sin(angle),
        ];
        out.push({ from, to, type: 'avenue' });
    }
}

/**
 * Organic (Asian/Old World) layout: irregular winding streets using random walks.
 * Narrow alleys, occasional wider roads. Uses noise-like random direction.
 */
function generateOrganicStreets(
    city: City,
    rng: () => number,
    latCorr: number,
    cityRadius: number,
    tier: PopulationTier,
    out: StreetSegment[],
): void {
    // Number of main organic roads emanating from center
    let numMainRoads = 5;
    let numBranches = 3;
    let stepsPerRoad = 8;

    if (tier === 'hamlet') { numMainRoads = 2; numBranches = 1; stepsPerRoad = 4; }
    else if (tier === 'town') { numMainRoads = 3; numBranches = 2; stepsPerRoad = 6; }
    else if (tier === 'metro' || tier === 'megalopolis') { numMainRoads = 8; numBranches = 4; stepsPerRoad = 12; }

    const stepLength = cityRadius * 0.12;
    const maxRadius = cityRadius * 0.65;

    for (let road = 0; road < numMainRoads; road++) {
        let angle = (road / numMainRoads) * Math.PI * 2 + (rng() - 0.5) * 0.5;
        let cx = city.x;
        let cy = city.y;

        for (let step = 0; step < stepsPerRoad; step++) {
            // Winding: add random deviation to angle
            angle += (rng() - 0.5) * 0.8;
            const dx = stepLength * Math.cos(angle) * latCorr;
            const dy = stepLength * Math.sin(angle);
            const nx = cx + dx;
            const ny = cy + dy;

            // Check if still within city radius
            const distFromCenter = Math.sqrt(
                ((nx - city.x) / latCorr) ** 2 + (ny - city.y) ** 2
            );
            if (distFromCenter > maxRadius) break;

            out.push({
                from: [cx, cy],
                to: [nx, ny],
                type: step < 2 ? 'avenue' : 'street',
            });

            // Branch off at some steps
            if (step > 0 && step % 2 === 0) {
                for (let br = 0; br < numBranches; br++) {
                    const brAngle = angle + (rng() - 0.5) * 2.0 + (br % 2 === 0 ? Math.PI / 3 : -Math.PI / 3);
                    let bx = nx;
                    let by = ny;
                    const branchSteps = 2 + Math.floor(rng() * 3);
                    const branchStep = stepLength * 0.6;
                    let brDir = brAngle;

                    for (let bs = 0; bs < branchSteps; bs++) {
                        brDir += (rng() - 0.5) * 1.0;
                        const nbx = bx + branchStep * Math.cos(brDir) * latCorr;
                        const nby = by + branchStep * Math.sin(brDir);
                        const brDist = Math.sqrt(
                            ((nbx - city.x) / latCorr) ** 2 + (nby - city.y) ** 2
                        );
                        if (brDist > maxRadius) break;
                        out.push({
                            from: [bx, by],
                            to: [nbx, nby],
                            type: 'street',
                        });
                        bx = nbx;
                        by = nby;
                    }
                }
            }

            cx = nx;
            cy = ny;
        }
    }
}

