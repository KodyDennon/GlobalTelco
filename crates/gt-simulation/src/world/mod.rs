    pub fn transfer_corporation_assets(&mut self, from: EntityId, to: EntityId) {
        // Transfer infrastructure nodes
        let nodes = self.corp_infra_nodes.shift_remove(&from).unwrap_or_default();
        for &node_id in &nodes {
            if let Some(node) = self.infra_nodes.get_mut(&node_id) {
                node.owner = to;
            }
            if let Some(own) = self.ownerships.get_mut(&node_id) {
                own.owner = to;
            }
        }
        self.corp_infra_nodes.entry(to).or_default().extend(nodes);

        // Transfer edge ownership
        for edge in self.infra_edges.values_mut() {
            if edge.owner == from {
                edge.owner = to;
            }
        }

        // Transfer contracts
        for contract in self.contracts.values_mut() {
            if contract.from == from {
                contract.from = to;
            }
            if contract.to == from {
                contract.to = to;
            }
        }

        // Merge financials
        let from_fin = self.financials.shift_remove(&from).unwrap_or(Financial {
            cash: 0,
            revenue_per_tick: 0,
            cost_per_tick: 0,
            debt: 0,
        });
        if let Some(to_fin) = self.financials.get_mut(&to) {
            to_fin.cash += from_fin.cash;
            to_fin.debt += from_fin.debt;
        }

        // Transfer workforce
        if let Some(from_wf) = self.workforces.shift_remove(&from) {
            if let Some(to_wf) = self.workforces.get_mut(&to) {
                to_wf.employee_count += from_wf.employee_count;
            }
        }

        // Transfer debt instruments
        for debt in self.debt_instruments.values_mut() {
            if debt.holder == from {
                debt.holder = to;
            }
        }

        // Remove the absorbed corporation
        self.corporations.shift_remove(&from);
        self.ai_states.shift_remove(&from);
        self.policies.shift_remove(&from);
        self.covert_ops.shift_remove(&from);
        self.achievements.shift_remove(&from);
    }

    pub(super) fn charge_per_unit_fees(&mut self, corp_id: EntityId) {
        let mut charges: Vec<(EntityId, EntityId, Money)> = Vec::new(); // (license_id, holder_corp, charge)

        for (&license_id, license) in self.licenses.iter() {
            if license.licensee_corp != corp_id {
                continue;
            }
            if !matches!(license.license_type, LicenseType::PerUnit) {
                continue;
            }
            if !license.is_active(self.tick) {
                continue;
            }
            let patent = match self.patents.get(&license.patent_id) {
                Some(p) => p,
                None => continue,
            };
            let charge = patent.per_unit_charge();
            if charge > 0 {
                charges.push((license_id, patent.holder_corp, charge));
            }
        }

        for (license_id, holder_corp, charge) in charges {
            // Deduct from licensee
            if let Some(fin) = self.financials.get_mut(&corp_id) {
                fin.cash -= charge;
            }
            // Pay to patent holder
            if let Some(fin) = self.financials.get_mut(&holder_corp) {
                fin.cash += charge;
            }
            // Record usage on the license
            if let Some(license) = self.licenses.get_mut(&license_id) {
                license.record_use(charge);
            }
        }
    }
