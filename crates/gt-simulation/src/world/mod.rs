#[derive(Copy, Clone, Debug, PartialEq, Serialize, Deserialize)]
pub struct SpatialNode {
    pub id: EntityId,
    pub pos: [f64; 2],
}

impl rstar::RTreeObject for SpatialNode {
    type Envelope = rstar::AABB<[f64; 2]>;
    fn envelope(&self) -> Self::Envelope {
        rstar::AABB::from_point(self.pos)
    }
}

impl rstar::PointDistance for SpatialNode {
    fn distance_2(&self, point: &[f64; 2]) -> f64 {
        let dx = self.pos[0] - point[0];
        let dy = self.pos[1] - point[1];
        dx * dx + dy * dy
    }
}

/// A predicted disaster event from the forecast system.
/// Generated by peeking ahead in the deterministic RNG to approximate
/// which regions will be hit by disasters in the next 500 ticks.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DisasterForecast {
    /// Region entity ID where the disaster is predicted.
    pub region_id: EntityId,
    /// Human-readable region name.
    pub region_name: String,
    /// Tick at which the disaster is predicted to occur.
    pub predicted_tick: Tick,
    /// Estimated probability (0.0-1.0) based on how decisively the RNG roll
    /// would trigger. Higher values indicate stronger certainty.
    pub probability: f64,
    /// Predicted disaster type name (e.g. "Earthquake", "Hurricane").
    pub disaster_type: String,
}

