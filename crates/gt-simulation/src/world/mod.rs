                if let Some(f) = self.terminal_factories.get_mut(&factory) {
                    let actual = count.min(f.produced_stored);
                    f.produced_stored -= actual;
                    if let Some(w) = self.warehouses.get_mut(&warehouse) {
                        w.terminal_inventory += actual;
                    }
                }
